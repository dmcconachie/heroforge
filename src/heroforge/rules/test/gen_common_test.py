"""
tests/test_gen_common.py
-------------------------
Unit tests for the shared code-generator helpers in
``heroforge.rules._gen_common``. These helpers underpin every
``check-*`` generator, so pinning their behaviour directly
(rather than only indirectly via idempotency tests) catches
regressions in the helpers themselves.
"""

from __future__ import annotations

from pathlib import Path

from heroforge.rules._gen_common import (
    check_or_fix,
    emit_header,
    emit_import,
    emit_member,
    enum_ident,
)

# ---------------------------------------------------------------------------
# enum_ident
# ---------------------------------------------------------------------------


def test_enum_ident_simple() -> None:
    assert enum_ident("Power Attack") == "POWER_ATTACK"


def test_enum_ident_apostrophe_is_stripped() -> None:
    assert enum_ident("Bag o' Gold") == "BAG_O_GOLD"


def test_enum_ident_plus_becomes_plus_word() -> None:
    assert enum_ident("Sword +1") == "SWORD_PLUS_1"


def test_enum_ident_ampersand_becomes_and() -> None:
    assert enum_ident("Bell & Whistle") == "BELL_AND_WHISTLE"


def test_enum_ident_punctuation_becomes_underscore() -> None:
    # parens, slash, comma, period, dash all collapse to a single
    # underscore after whitespace normalisation.
    assert enum_ident("Foo (bar)") == "FOO_BAR"
    assert enum_ident("Foo/Bar") == "FOO_BAR"
    assert enum_ident("Foo, Bar") == "FOO_BAR"
    assert enum_ident("Foo.Bar") == "FOO_BAR"
    assert enum_ident("Foo-Bar") == "FOO_BAR"


def test_enum_ident_collapses_runs_of_whitespace() -> None:
    assert enum_ident("Foo   Bar") == "FOO_BAR"
    assert enum_ident("  Foo  Bar  ") == "FOO_BAR"


# ---------------------------------------------------------------------------
# emit_member
# ---------------------------------------------------------------------------


def test_emit_member_short_is_single_line() -> None:
    out = emit_member("FOO", "foo")
    assert out == ['    FOO = "foo"']
    assert len(out[0]) <= 80


def test_emit_member_long_wraps_to_three_lines() -> None:
    long_val = "x" * 90
    out = emit_member("FOO", long_val)
    assert out == [
        "    FOO = (",
        f'        "{long_val}"',
        "    )",
    ]


def test_emit_member_at_80_col_boundary_stays_single_line() -> None:
    # Single-line form = 9 + len(ident) + len(val); pick lengths
    # that land exactly on 80.
    ident = "A" * 10
    val = "b" * 61
    single = f'    {ident} = "{val}"'
    assert len(single) == 80
    assert emit_member(ident, val) == [single]


