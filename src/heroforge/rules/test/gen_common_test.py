def test_check_or_fix_returns_true_when_up_to_date(
    tmp_path: Path,
) -> None:
    target = tmp_path / "out.py"
    target.write_text("hello\n")
    desired = {target: "hello\n"}
    assert check_or_fix(desired, fix=False, checker_name="x", base_dir=tmp_path)
    # Content unchanged.
    assert target.read_text() == "hello\n"


def test_check_or_fix_returns_false_on_drift_without_fix(
    tmp_path: Path,
) -> None:
    target = tmp_path / "out.py"
    target.write_text("stale\n")
    desired = {target: "fresh\n"}
    assert not check_or_fix(
        desired, fix=False, checker_name="x", base_dir=tmp_path
    )
    # fix=False must not mutate the file.
    assert target.read_text() == "stale\n"


def test_check_or_fix_writes_when_fix_is_true(tmp_path: Path) -> None:
    target = tmp_path / "out.py"
    target.write_text("stale\n")
    desired = {target: "fresh\n"}
    # Still returns False (the *pre-call* state was stale).
    assert not check_or_fix(
        desired, fix=True, checker_name="x", base_dir=tmp_path
    )
    assert target.read_text() == "fresh\n"


def test_check_or_fix_creates_missing_file_when_fix_is_true(
    tmp_path: Path,
) -> None:
    target = tmp_path / "nested" / "out.py"
    target.parent.mkdir()
    desired = {target: "fresh\n"}
    assert not check_or_fix(
        desired, fix=True, checker_name="x", base_dir=tmp_path
    )
    assert target.read_text() == "fresh\n"


def test_check_or_fix_reports_each_drifting_file(
    tmp_path: Path, capsys: object
) -> None:
    a = tmp_path / "a.py"
    b = tmp_path / "b.py"
    a.write_text("old-a\n")
    b.write_text("new-b\n")
    desired = {a: "new-a\n", b: "new-b\n"}
    assert not check_or_fix(
        desired, fix=False, checker_name="check-x", base_dir=tmp_path
    )
    err = capsys.readouterr().err  # type: ignore[attr-defined]
    assert "check-x" in err
    assert "a.py" in err
    assert "b.py" not in err
