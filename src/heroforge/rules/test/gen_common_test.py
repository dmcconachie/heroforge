def test_emit_import_short_is_single_line() -> None:
    out = emit_import(import_path="foo.bar", import_name="Baz")
    assert out == ["from foo.bar import Baz"]


def test_emit_import_long_wraps_to_three_lines() -> None:
    long_path = "a.very." + "long." * 15 + "module"
    out = emit_import(import_path=long_path, import_name="Thing")
    assert out == [
        f"from {long_path} import (",
        "    Thing,",
        ")",
    ]


# ---------------------------------------------------------------------------
# emit_header
# ---------------------------------------------------------------------------


def test_emit_header_basic() -> None:
    lines = emit_header(
        filename="foo.py",
        description="Test file.",
        generator_file="_gen_foo.py",
        toml_command="check-foo",
        cls_name="KnownFoo",
    )
    assert lines[0] == '"""'
    assert "foo.py" in lines
    assert "Test file." in lines
    assert "AUTO-GENERATED by _gen_foo.py." in lines
    assert "``uv run check-foo --fix``." in lines
    assert "from enum import StrEnum" in lines
    assert lines[-1] == "class KnownFoo(StrEnum):"


def test_emit_header_with_extra_imports_places_them_before_class() -> None:
    lines = emit_header(
        filename="foo.py",
        description="Test file.",
        generator_file="_gen_foo.py",
        toml_command="check-foo",
        extra_imports=[
            ("heroforge.rules.combine_str_enum", "combine"),
            ("heroforge.rules.core.skills", "KnownCoreSkill"),
        ],
        cls_name="KnownFoo",
    )
    enum_idx = lines.index("from enum import StrEnum")
    class_idx = lines.index("class KnownFoo(StrEnum):")
    combine_idx = lines.index(
        "from heroforge.rules.combine_str_enum import combine"
    )
    skills_idx = lines.index(
        "from heroforge.rules.core.skills import KnownCoreSkill"
    )
    assert enum_idx < combine_idx < class_idx
    assert enum_idx < skills_idx < class_idx


